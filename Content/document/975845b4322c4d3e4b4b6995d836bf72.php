The key specific objectives of ReMaT are:
</p>
<ul>
<li>
<p>
To develop a programme of research and project management training with six different course modules on topics tailored to the needs of young European
research Ph.D. students working on international projects
</p>
</li>
<li>
<p>
To deliver a <b>pilot series of four training workshops</b>; which will be used to assess the effectiveness of the developed training programme and also
permit young researchers to extend their networks of personal contacts
</p>
</li>
<li>
<p>
Two focus group workshops with trainers
</p>
</li>
<li>
<p>
To carry out a <b>best-practice workshop</b> on young researcher management training conference for research organisations (target group are typically
the heads of Ph.D. programmes at the different European universities and research institutes)
</p>
</li>
<li>
<p>
To provide information highlighting opportunities for training around Europe relevant to the needs of academic researchers using existing dissemination channels.
</p>
</li>
</ul>
<p>
The experiences of the<b> module-based training course these will be presented and discussed in two focus group workshops.</b> These<b> pilot workshops </b>
will be<b> carefully monitored </b>and the final form of the training materials will be<b> adjusted according to the feedback received from the Ph.D. participants.</b>
The final version of the module-based training together with a possible implementation strategy will be presented to the heads of the Ph.D. programmes at the
best-practice conference held at the end of the project.
</p>
<p>
The best-practice conference will be used further dissemination of the materials developed during ReMaT, <b>sharing the experience gained through the 
training workshops and finally, it will stimulate the exchange of experience among the heads of the Ph.D. programmes in Europe</b> and it will provide the
<b>basic feedback on similarities and disparities of national Ph.D. programmes.</b>
</p>
<p>Such feedback may support the European Commission in its efforts for unification of education systems and acceptance of degrees in different Member
States, which would further increase the mobility of the young scientists within EU.
</p>