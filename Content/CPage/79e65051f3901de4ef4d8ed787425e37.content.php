<p>
    The four pilot training courses will be held in co-operation with local universities and research institutes throughout Europe.
</p>
<p>
    The
    <b>
        training courses
    </b>
    will be announced through the listed dissemination channels well in advance. The number of participants will have a ceiling
    of 25 to reach maximum of impact and still keep the interactivity of the workshop participants at a reasonable level.
    <br/>
    The training courses will be characterised by
    <ul>
        <li>
            A high level of interaction between trainers and participants and between the participants themselves
        </li>
        <li>
            Extensive use of case studies to ensure learning by using practical examples
        </li>
        <li>
            A hands-on approach, which will aim to induce practical knowledge and experiences rather than theoretical knowledge.
        </li>
    </ul>
    The performance of the training courses will be carefully monitored and the feedback from the training course participants will provide essential
    inputs to refine the final version of the training materials. These will also provide input to two focus group workshops for potential trainers
    and PhD programme directors. At the end of the training course the
    <b>
        participants will be asked to fill in a complex evaluation questionnaire prepared
        by the ReMaT consortium
    </b>
    .
</p>
<p>
    The
    <b>
        questionnaire
    </b>
    will be developed in conjunction with experienced market researchers and include such topics as:
</p>
<ul>
    <li>
        Quality of the trainers and training materials
    </li>
    <li>
        Relevance of the topics and level of training
    </li>
    <li>
        Amount of time devoted to different topics
    </li>
    <li>
        Proposals for additional, redundant or overlapping topics
    </li>
    <li>
        Course topics rating according to their importance to the participants
    </li>
    <li>
        Effectiveness of the training course methodology
    </li>
</ul>