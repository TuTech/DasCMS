The intensive
workshops have been designed to offer an introduction to research management
through a series of interactive modules on complementary skills-based training.
Staged over two days, the interactive pilot workshops are open to PhD students
from across Europe, and will provide an excellent networking opportunity in
addition to the training programme itself.
<p>
</p>
</span>
</p>
<p style="" style="" style="text-align: justify;" style="">
<span style="">
The modules focus on
a range of issues from knowledge creation in research based environments, to
knowledge exploitation in commercial environments. Spanning a wide range of
issues the modules are intended to provide an overview of key issues associated
with the management of research projects needed to work in academia and
industry. In addition to the modules, there will be an open forum with
‘enterprising academics’, who have worked in or with industry, and/or
commercialised their own research, effectovely with the opportunity
for participants to ask questions.&nbsp;
<p>
</p>
</span>
</p>
<p style="" style="text-align: justify;">
<span style="">
In short, ReMaT
intends to provide a foundation complementary skills for managing research
through informal interactive teaching in small groups. The objective is to
introduce participants to research management, providing an overview of key
issues relevant to PhD students and early career researchers.
